public class SubNode {
    private String city;
    private int airialDistance;
    private int walkDistance;
    private int carDistance;

    public SubNode() {
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getAirialDistance() {
        return airialDistance;
    }

    public void setAirialDistance(int airialDistance) {
        this.airialDistance = airialDistance;
    }

    public int getWalkDistance() {
        return walkDistance;
    }

    public void setWalkDistance(int walkDistance) {
        this.walkDistance = walkDistance;
    }

    public int getCarDistance() {
        return carDistance;
    }

    public void setCarDistance(int carDistance) {
        this.carDistance = carDistance;
    }


}
